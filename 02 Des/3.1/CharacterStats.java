public class CharacterStats {
    private String name;
    private int hp;

    public String setName(String name) {
        return this.name = name;
    }

    public int setHp(int hp) {
        return this.hp = hp;
    }

    public String getName() {
        return name;
    }
    
    public int getHp() {
        return hp;
    }
}